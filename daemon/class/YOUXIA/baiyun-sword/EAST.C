#include <ansi.h>

inherit SSERVER;

int perform(object me, object target)
{
        object weapon;
        int i;
        if( !target ) target = offensive_target(me);

        if( !target
        ||      !target->is_character()
        ||      target->is_corpse()
        ||      target==me)
                return notify_fail("你要对谁施展这一招「"HIC"一剑东来"NOR"」？\n");

        if(!me->is_fighting())
                return notify_fail("「"HIC"一剑东来"NOR"」只能在战斗中使用！\n");

        if((int)me->query("max_force") < 1000 )
                return notify_fail("你的内力不够！\n");
                if(me->query("family/family_name") != "江湖浪子" )
                return notify_fail("你是哪儿偷学来的武功，也想用「"HIC"一剑东来"NOR"」?\n");
        if((int)me->query("force") < 1000 )
                return notify_fail("你的内力不足！\n");

        if((int)me->query("sen") < 500 )
                return notify_fail("你的精神不足，没法子施用外功！\n");

        i=(int)me->query_skill("baiyun-sword",1)+(int)me->query_kar();

        if( i < 100)
                return notify_fail("你的白云剑法修为还不够，无法施展「"HIC"一剑东来"NOR"」！\n");

        message_vision(HIW"\n$N气运丹田，使出一招叶孤城的绝学「"HIC"一剑东来"NOR"」！\n"NOR,me);

	me->delete("env/brief_message");
	target->delete("env/brief_message");

        me->set_temp("QJB_perform", 7);
        COMBAT_D->do_attack(me, target, me->query_temp("weapon"));

        me->delete_temp("QJB_perform");

        me->receive_damage("sen", 100+random(100));
        me->add("force", -200);

        if( !target->is_fighting(me) ) {
                if( living(target) ) {
                        if( userp(target) ) target->fight_ob(me);
                        else target->kill_ob(me);
                }
        }

        me->start_busy(1);
        return 1;
}

